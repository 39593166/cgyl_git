package com.utoo.chunguanyouli.bean;

public class VersionInfo {
	private int id;

	private int now;

	private int min;

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return this.id;
	}

	public void setNow(int now) {
		this.now = now;
	}

	public int getNow() {
		return this.now;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMin() {
		return this.min;
	}

}